import React from 'react';
import { useNavigate } from 'react-router-dom';  // Import useNavigate for navigation
import './PhysicalSecurity.css'; // Reuse the same CSS file for consistency in styling
import logo from '../assets/MachaLogo.png';  // Adjust the path relative to the current file location

function PolicyAndCompliancePage() {
  const navigate = useNavigate();  // Initialize useNavigate hook

  const handleButtonClick = (section) => {
    // Navigate to specific routes based on section
    switch (section) {
      case 'Policy Development':
        navigate('/policy-development');
        break;
      case 'Compliance Management**':
        navigate('/compliance-management');
        break;
      case 'Branch 4: Policy Review and Revision':
        navigate('/branch-4');
        break;
      default:
        console.log('Unknown section');
    }
  };

  return (
    <div className="form-page">
      {/* Header Section */}
      <header className="header">
        <button className="back-button" onClick={() => window.history.back()}>‚Üê</button> {/* Use window.history.back for navigation */}
        <h1>The MACHA Group</h1>
        <img src={logo} alt="Logo" className="logo" />
      </header>

      {/* Policy and Compliance Section */}
      <main className="form-container">
        <h2>Policy and Compliance</h2>
        <form>
          {/* Policy and Compliance Buttons */}
          {['Policy Development', 'Compliance Management**', 'Branch 4: Policy Review and Revision'].map((section, index) => (
            <div key={index} className="form-section">
              <label>{section}</label>
              <button type="button" className="form-button" onClick={() => handleButtonClick(section)}>
                Enter Here
              </button>
            </div>
          ))}
        </form>
      </main>
    </div>
  );
}

export default PolicyAndCompliancePage;