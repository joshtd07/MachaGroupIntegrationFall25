import React from 'react';
import { useNavigate } from 'react-router-dom';  // Import useNavigate for navigation
import './AccessControl.css'; // Reuse the same CSS file for consistency in styling
import logo from '../assets/MachaLogo.png';  // Adjust the path relative to the current file location

function JointTrainingExercisesPage() {
  const navigate = useNavigate();  // Initialize useNavigate hook

  const handleButtonClick = (section) => {
    console.log(`Button clicked for: ${section}`);
    // Add logic for handling button click, e.g., open a modal or navigate

    switch (section) {
      case 'Active Shooter Drills with Police':
        navigate('/active-shooter-drills-with-police');
        break;
      case 'Emergency Response Training':
        navigate('/emergency-response-training2');
        break;
      case 'Crisis Intervention Workshops':
        navigate('/crisis-intervention-workshops');
        break;
      default:
        console.log('Unknown section');
    }
  };

  return (
    <div className="form-page">
      {/* Header Section */}
      <header className="header">
        <button className="back-button" onClick={() => window.history.back()}>‚Üê</button> {/* Use window.history.back for navigation */}
        <h1>The MACHA Group</h1>
        <img src={logo} alt="Logo" className="logo" />
      </header>

      {/* Joint Training Exercises Section */}
      <main className="form-container">
        <h2>Joint Training Exercises</h2>
        <form>
          {/* Joint Training Exercises Buttons */}
          {['Active Shooter Drills with Police', 'Emergency Response Training', 'Crisis Intervention Workshops'].map((section, index) => (
            <div key={index} className="form-section">
              <label>{section}</label>
              <button type="button" className="form-button" onClick={() => handleButtonClick(section)}>
                Enter Here
              </button>
            </div>
          ))}
        </form>
      </main>
    </div>
  );
}

export default JointTrainingExercisesPage;